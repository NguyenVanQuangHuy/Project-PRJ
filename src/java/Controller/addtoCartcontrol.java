    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        HttpSession session = request.getSession();
        Card card = null;
        Object o = session.getAttribute("card");
        
        if(o!= null){
            card = (Card)o;
        }
        else {
            card = new Card();
        }
        int id = Integer.parseInt(request.getParameter("aid"));
        Productdao pd = new Productdao();
        pd.getProductbyId(id);
        Product p = pd.getProducts() ;
        int quantity = 1;
        Item t = new Item(quantity, p);
        card.addItem(t);
        
        List<Item> list = card.getItems();
        session.setAttribute("card", card);
        session.setAttribute("size", list.size());
        request.getRequestDispatcher("Views/orderdet.jsp").forward(request, response);
    } 

